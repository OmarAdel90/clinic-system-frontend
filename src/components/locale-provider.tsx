function translatePattern(text: string): string | null {
  const pageHeaderPattern = [
    [/^CRM and clinic operations in (.+)\.$/, "إدارة العملاء وعمليات العيادة بتوقيت $1."],
    [/^Completed visit reports in (.+)\.$/, "تقارير الزيارات المكتملة بتوقيت $1."],
    [/^Conversations and performance in (.+)\.$/, "المحادثات والأداء بتوقيت $1."],
    [/^Invoices and payments in (.+)\.$/, "الفواتير والمدفوعات بتوقيت $1."],
    [/^Patient feedback in (.+)\.$/, "ملاحظات المرضى بتوقيت $1."],
    [/^Treatment plans and scheduled visits in (.+)\.$/, "خطط العلاج والزيارات المجدولة بتوقيت $1."],
    [/^Visit operations in (.+)\.$/, "عمليات الزيارات بتوقيت $1."],
  ] as const;

  for (const [pattern, replacement] of pageHeaderPattern) {
    if (pattern.test(text)) {
      return text.replace(pattern, replacement);
    }
  }

  const dynamicPattern = [
    [/^Showing ([\d,]+)-([\d,]+) of ([\d,]+) (.+)$/, "عرض $1-$2 من $3 $4"],
    [/^Page ([\d,]+) \/ ([\d,]+)$/i, "صفحة $1 / $2"],
    [/^Position (.+)$/, "الترتيب $1"],
    [/^User ID: (.+)$/, "رقم المستخدم: $1"],
    [/^Phone: (.+)$/, "الهاتف: $1"],
    [/^Title: (.+)$/, "المسمى: $1"],
    [/^Key: (.+)$/, "المفتاح: $1"],
    [/^Order: (.+)$/, "الترتيب: $1"],
    [/^Qualified: (.+)$/, "مؤهل: $1"],
    [/^Created: (.+)$/, "تاريخ الإنشاء: $1"],
    [/^Last touch: (.+)$/, "آخر تواصل: $1"],
    [/^First touch: (.+)$/, "أول تواصل: $1"],
    [/^Converted: (.+)$/, "تم التحويل: $1"],
    [/^Clinic: (.+)$/, "العيادة: $1"],
    [/^Agent: (.+)$/, "الموظف: $1"],
    [/^Records: (.+)$/, "السجلات: $1"],
    [/^Issued: (.+)$/, "تاريخ الإصدار: $1"],
    [/^Total: (.+)$/, "الإجمالي: $1"],
    [/^Paid: (.+)$/, "المدفوع: $1"],
    [/^Lead #(.+)$/, "عميل #$1"],
    [/^Clinic #(.+)$/, "عيادة #$1"],
    [/^Invoice #(.+)$/, "فاتورة #$1"],
    [/^Conversation #(.+)$/, "محادثة #$1"],
    [/^Status "(.+)" created successfully\.$/, "تم إنشاء الحالة \"$1\" بنجاح."],
    [/^Status "(.+)" updated successfully\.$/, "تم تحديث الحالة \"$1\" بنجاح."],
    [/^Status #(.+) deleted successfully\.$/, "تم حذف الحالة #$1 بنجاح."],
    [/^Payment recorded for invoice #(.+)\.$/, "تم تسجيل دفعة للفاتورة #$1."],
  ] as const;

  for (const [pattern, replacement] of dynamicPattern) {
    if (pattern.test(text)) {
      return text.replace(pattern, replacement);
    }
  }

  return null;
}

function translateText(text: string | null | undefined, locale: Locale): string {
  if (!text || locale === "en") {
    return text ?? "";
  }

  return exactTranslations[text] ?? translatePattern(text) ?? text;
}

const LocaleContext = createContext<LocaleContextValue | null>(null);

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    const saved = window.localStorage.getItem(STORAGE_KEY);
    if (saved === "ar" || saved === "en") {
      setLocaleState(saved);
    }
  }, []);

  useEffect(() => {
    const root = document.documentElement;
    root.lang = locale === "ar" ? "ar" : "en";

    window.localStorage.setItem(STORAGE_KEY, locale);
  }, [locale]);

  const value = useMemo<LocaleContextValue>(
    () => ({
      locale,
      isRTL: locale === "ar",
      setLocale: (nextLocale) => setLocaleState(nextLocale),
      toggleLocale: () => setLocaleState((current) => (current === "en" ? "ar" : "en")),
      t: (text) => translateText(text, locale),
    }),
    [locale],
  );

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error("useLocale must be used within LocaleProvider.");
  }

  return context;
}
